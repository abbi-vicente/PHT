// Variables
// let - mutable or changeable variable
let myName = "Abbi";
console.log(myName);

myName = "Isabel";
console.log(myName);

// const - unchangeable variable
const myYear = 2024;
console.log(myYear);

// myYear = 2025;
// console.log(myYear); - will give an error

// var
var myHobby = "sleeping";
console.log(myHobby);

myHobby = "eating";
console.log(myHobby);

// console.log(MyHobby); - different from myHobby

