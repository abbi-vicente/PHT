// Data Types - Primitive Types
// Dynamic Typing
// Number -
let number = 13;
number = 13.34;
number = -12;
console.log("Number: ", number);

// String - texts '' or ""
let word = "Hello, world!";
// hello = 'Hello Javascript's world' - best practice to use double quotes
console.log("String: ", word);

// Boolean - true or false
let status = true;
status = false;
console.log("Boolean: ", status);

// Undefined - uninitialized or missing value
let nothing;
console.log(nothing);

// Null
let nullVar = null;
console.log(nullVar);

